package skuniv.ac.di.vo;

public class CardVO {
	private String title;
	private String writer;
	private String date;
	private int file_to_have;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getFile_to_have() {
		return file_to_have;
	}
	public void setFile_to_have(int file_to_have) {
		this.file_to_have = file_to_have;
	}
	

}
